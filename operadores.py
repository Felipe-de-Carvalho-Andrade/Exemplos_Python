# Operadores Aritméticos
a = 10
b = 3
add_result = a + b
sub_result = a - b
mul_result = a * b
div_result = a / b
div_int_result = a // b
mod_result = a % b
exp_result = a ** b

# Operadores de Atribuição
x = 5
x += 3
y = 10
y -= 2

# Operadores de Comparação
num1 = 5
num2 = 3
equal_result = num1 == num2
greater_result = num1 > num2

# Operadores Lógicos
p = True
q = False
and_result = p and q
or_result = p or q
not_result = not p

# Operadores de Identidade
list1 = [1, 2, 3]
list2 = list1
is_result = list1 is list2

# Operadores de Associação em Membro
numbers = [1, 2, 3, 4, 5]
in_result = 3 in numbers
not_in_result = 6 not in numbers

# Impressão dos resultados
print("Operadores Aritméticos:")
print(add_result, sub_result, mul_result, div_result, div_int_result, mod_result, exp_result)

print("\nOperadores de Atribuição:")
print(x, y)

print("\nOperadores de Comparação:")
print(equal_result, greater_result)

print("\nOperadores Lógicos:")
print(and_result, or_result, not_result)

print("\nOperadores de Identidade:")
print(is_result)

print("\nOperadores de Associação em Membro:")
print(in_result, not_in_result)
